                      var repertoire_id = element.parent().attr('repertoire_id');
                      if (repertoire_id == item.getRepertoireId() && parseInt(element.children('info').attr('avaiable_places')) <= 1) {
                        $('#path_' + element.attr('id')).trigger('item_select');
                      }
                    }
                  }
